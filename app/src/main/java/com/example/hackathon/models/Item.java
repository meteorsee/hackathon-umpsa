package com.example.hackathon.models;

public class Item {
    private String title;
    private String description;

    // Constructors
    public Item(String title) {
        this.title = title;
        this.description = description;
    }

    // Getters
    public String getTitle() {
        return title;
    }

    public String getDescription() {
        return description;
    }
}
